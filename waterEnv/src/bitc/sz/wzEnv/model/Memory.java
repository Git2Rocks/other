package bitc.sz.wzEnv.model;


public class Memory {
	private String total_mem;
	private String use_mem;
	private String free_mem;
	public String getTotal_mem() {
		return total_mem;
	}
	public void setTotal_mem(String total_mem) {
		this.total_mem = total_mem;
	}
	public String getUse_mem() {
		return use_mem;
	}
	public void setUse_mem(String use_mem) {
		this.use_mem = use_mem;
	}
	public String getFree_mem() {
		return free_mem;
	}
	public void setFree_mem(String free_mem) {
		this.free_mem = free_mem;
	}
	
}
